export function petsciiUpper2Str(petsciiCharCodes) {
    var uniCharCodes = [];
    var petsciiCharCodesLength = petsciiCharCodes.length;
    for (var i = 0; i < petsciiCharCodesLength; ++i) {
        uniCharCodes.push(petsciiUpper2unicode[petsciiCharCodes[i]]);
    }
    return String.fromCharCode.apply(null, uniCharCodes);
}

